class CAritmetica01
{
  /*operaciones aritméticas*/
  public static void main (String[] args)
  {  int dato1, dato2, dato3, resultado;
    dato1 = 20;
    dato2 = 10;
    //suma
    resultado = dato1 + dato2;
    System.out.println(dato1 + " + "+ dato2 +" = "+resultado);
    //resta
    resultado = dato1 - dato2;
    System.out.println(dato1 + " - "+ dato2 +" = "+resultado);
    //multiplicacion
    resultado = dato1 * dato2;
    System.out.println(dato1 + " * "+ dato2 +" = "+resultado);
    //división
    resultado = dato1 / dato2;
    System.out.println(dato1+ " / "+ dato2 +" = "+resultado);
  }
}
