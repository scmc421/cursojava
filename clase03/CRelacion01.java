class CRelacion01{
  public static void main(String[] args) {
    int num = 24;
    if (num % 2 == 0) // Par si el residuo de la división es cero
      {
        System.out.println("Número par, Valor: " + num);
      }
    int num2 = 23;
    if (num2 % 2 != 0) //Impar si el residuo de la división es cero
      {
        System.out.println("Número IMpar, Valor: " + num2);
      }
  }
}
